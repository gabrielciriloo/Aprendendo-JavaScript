const resultado = prompt("Escolha uma alternativa:\nA\nB\nC")

switch (resultado) {
    case "A": 
    alert("Resultado 'A'")
    break
    case "B": 
    alert("Resultado 'B'")
    break
    case "C": 
    alert("resultado 'C'")
    break
    default: 
    alert("Finalizado")
}


